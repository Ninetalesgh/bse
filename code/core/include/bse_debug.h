    class ProfilingObject
    {
    public:
      ProfilingObject( u64* output ) : output( output ) { *output =  __rdtsc(); }
      ~ProfilingObject() { *output = __rdtsc() - *output; }
    private:
      u64* output;
    };
    #endif

